#include "libft.h"
#include "strs.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	l_len;
	size_t	i;

	l_len = ft_strlen(little);
	if (l_len == 0)
		return ((char *)big);
	if (len == 0)
		return (NULL);
	i = 0;
	while (i + l_len <= len && big[i])
	{
		if (ft_strncmp(&big[i], little, l_len) == 0)
			return ((char *)&big[i]);
		i++;
	}
	return (NULL);
}
